package com.myrpc.leafe.exceptions;

public class XPathExpressionExceptionException extends RuntimeException{

    public XPathExpressionExceptionException() {
    }
    public XPathExpressionExceptionException(Throwable cause) {
        super(cause);
    }
    public XPathExpressionExceptionException(String msg) {
        super(msg);
    }
    public XPathExpressionExceptionException(String msg, Throwable cause) {
        super(msg,cause);
    }
}
